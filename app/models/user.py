from .db import db
from werkzeug.security import generate_password_hash, check_password_hash
from flask_login import UserMixin


class User(db.Model, UserMixin):
    __tablename__ = 'users'

    id = db.Column(db.Integer, primary_key=True)
    first_name = db.Column(db.String(40), nullable=False)
    last_name = db.Column(db.String(40), nullable=False)
    email = db.Column(db.String(255), nullable=False, unique=True)
    hashed_password = db.Column(db.String(255), nullable=False)

    blocks = db.relationship("Block", back_populates="user")
    rooms = db.relationship("Room", \
        primaryjoin="or_(User.id==Room.user_id, User.id==Room.friend_id)", \
        viewonly=True)
    chats = db.relationship("Chat", back_populates="user")

    @property
    def password(self):
        return self.hashed_password

    @password.setter
    def password(self, password):
        self.hashed_password = generate_password_hash(password)

    def check_password(self, password):
        return check_password_hash(self.password, password)

    def to_dict(self):
        return {
            'id': self.id,
            'first_name': self.first_name,
            'last_name': self.last_name,
            'email': self.email,
            "blocks": {block.id: block.to_dict() for block in self.blocks},
            "rooms": {room.id: room.to_dict() for room in self.rooms}
        }
